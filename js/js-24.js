// break and continue keyword

// BREAK
for(let i=0;i<11;i++){
    if(i===4){
        break; // break loop and exit from loop when i is 4
    }
     console.log(i);
}
console.log("Hello there");
for(let i=0;i<11;i++){
    if(i===4){
        continue; // break again send to start loop and continue from loop when i is 4
    }
     console.log(i);
}
console.log("Hello there");