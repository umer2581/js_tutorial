//to find string of a length we use .LENGTH with variable

let str1="mo umer"
console.log(str1.length);

//to find last character of value we use .LENGTH-1 

let str2="dfjkldfkldlkjfasljdflasjr";
console.log(str2[str2.length-1]);
//to find any character of value we use .LENGTH-n of numbers

let str3="dfjkldfkldlkjfasljdflasjr";
console.log(str3[str3.length-4]);